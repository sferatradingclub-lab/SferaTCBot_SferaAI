"""
Скрипт-инструкция для создания видео с правильными параметрами для Telegram бота
"""

def main():
    print("Инструкция по созданию видео для Telegram бота SferaTC")
    print("=" * 50)
    print()
    print("Для автоматического воспроизведения и зацикливания видео в Telegram,")
    print("видео должно соответствовать следующим техническим требованиям:")
    print()
    print("1. Формат: MP4")
    print("2. Кодек видео: H.264")
    print("3. Без аудиодорожки (без звука)")
    print("4. Разрешение: до 1920x1920 (рекомендуется 1280x720 или 640x360)")
